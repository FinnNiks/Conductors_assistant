
public class data {
private String stName;
private String time;
private String psNumber;
public data(String psNumber, String time, String stName) {
	super();
	this.stName = stName;
	this.time = time;
	this.psNumber = psNumber;
}
public String getStName() {
	return stName;
}
public String getTime() {
	return time;
}
public String getPsNumber() {
	return psNumber;
}



}
